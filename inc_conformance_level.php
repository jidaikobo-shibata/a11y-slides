	</table>
</div>

<div>
	<h2>対応度</h2>
	<p>適合レベルと合わせて設定する。「準拠」「一部準拠」「配慮」がある。</p>
	<table class="tbl" style="margin-top:20px !important;">
	<thead>
	<tr>
		<th>表記</th>
		<th>方針の提示又は公開</th>
		<th>目標とする適合レベルの達成基準の試験結果</th>
	</tr>
	</thead>
	<tr>
		<th>準拠</th>
		<td>必須</td>
		<td>試験を実施し、達成基準を全て満たしていることを確認した上で、試験結果を公開する</td>
	</tr>
	<tr>
		<th>一部準拠</th>
		<td>必須</td>
		<td>試験を実施し、達成基準の一部を満たしていることを確認する。試験結果の公開は任意</td>
	</tr>
	<tr>
		<th>配慮</th>
		<td>必須</td>
		<td>試験の実施の有無、結果は問わない</td>
	</tr>
	</table>
</div>
