	<p>アクセシビリティ対応の度合いを決定する。3段階ある。</p>
	<table class="tbl" style="margin-top:20px !important;">
	<tr>
		<th style="width: 4em; text-align: center;">A</th>
		<td>最低限達成すべき適合レベル。Aの未達成は、情報を全く取得できない人が存在しうる</td>
	</tr>
	<tr>
		<th style="text-align: center;">AA</th>
		<td>達成することが望ましい適合レベル。<!-- 支援技術を使わなくてもアクセスできるようにすることを目指す。 @bakera https://barrierfree.nict.go.jp/topic/explanation/20231129/page2.html -->公的機関では義務として達成すべき</td>
