<h2>AS（アクセシビリティ サポーテッド）でない / 適合しない技術で作られている場合と、4つの特別な達成基準</h2>
<div>
<ul>
	<li><strong>ブラウザと支援技術の利用を阻害し、まったくコンテンツを利用できなくなる危険性のある実装を禁じる適合要件</strong></li>
	<li>ASでない、適合しない技術によって、他の情報へのアクセスが阻害されないようにする</li>
	<li><strong>依存していない技術</strong>は、適合要件を阻害しない</li>
	<li><strong>4つの特別な達成基準</strong>があり、この中には、事前の知識がないと、さらっと実装してしまうことがあるので注意が必要</li>
</ul>
